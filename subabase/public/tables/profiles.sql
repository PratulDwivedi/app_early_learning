create table public.profiles (
  id uuid not null,
  email text not null,
  user_name text null,
  full_name text null,
  data jsonb null,
  metadata jsonb null,
  access_control jsonb null,
  is_active boolean not null default true,
  tenant_id bigint not null,
  created_by bigint not null,
  created_at timestamp with time zone not null default now(),
  updated_by bigint null,
  updated_at timestamp with time zone null,
  uid bigint generated by default as identity not null,
  constraint profiles_pkey primary key (id, email, uid),
  constraint profiles_id_unique unique (id),
  constraint profiles_uid_key unique (uid),
  constraint profiles_id_fkey foreign KEY (id) references auth.users (id) on update CASCADE on delete CASCADE
) TABLESPACE pg_default;